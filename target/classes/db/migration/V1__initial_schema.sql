-- EarnX-3 PostgreSQL initial schema
-- =============================================================================

CREATE EXTENSION IF NOT EXISTS pgcrypto;

CREATE TABLE IF NOT EXISTS users (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    uuid VARCHAR(36) NOT NULL UNIQUE,
    created_at TIMESTAMPTZ NOT NULL,
    updated_at TIMESTAMPTZ NOT NULL,
    version BIGINT,
    phone VARCHAR(255) UNIQUE,
    email VARCHAR(255) NOT NULL UNIQUE,
    name VARCHAR(255) NOT NULL,
    password VARCHAR(255) NOT NULL,
    role VARCHAR(50) NOT NULL DEFAULT 'USER',
    profile_picture_url VARCHAR(1024),
    date_of_birth DATE,
    address VARCHAR(512),
    city VARCHAR(100),
    state VARCHAR(100),
    pincode VARCHAR(10),
    is_blocked BOOLEAN NOT NULL DEFAULT false,
    is_deleted BOOLEAN NOT NULL DEFAULT false,
    is_email_verified BOOLEAN NOT NULL DEFAULT false,
    is_phone_verified BOOLEAN NOT NULL DEFAULT false,
    last_login_at TIMESTAMPTZ,
    last_login_ip VARCHAR(64),
    referral_code VARCHAR(50) UNIQUE,
    referred_by_code VARCHAR(50),
    admin_notes TEXT,
    blocked_at TIMESTAMPTZ,
    block_reason VARCHAR(512)
);

CREATE INDEX IF NOT EXISTS idx_user_email ON users(email);
CREATE INDEX IF NOT EXISTS idx_user_phone ON users(phone);
CREATE INDEX IF NOT EXISTS idx_user_ref ON users(referral_code);
CREATE INDEX IF NOT EXISTS idx_user_role ON users(role);

CREATE TABLE IF NOT EXISTS refresh_tokens (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    token VARCHAR(512) NOT NULL UNIQUE,
    user_id BIGINT NOT NULL REFERENCES users(id) ON DELETE CASCADE,
    expires_at TIMESTAMPTZ NOT NULL,
    revoked BOOLEAN NOT NULL DEFAULT false
);

CREATE INDEX IF NOT EXISTS idx_rt_user ON refresh_tokens(user_id);

CREATE TABLE IF NOT EXISTS otps (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    identifier VARCHAR(255) NOT NULL,
    code_hash VARCHAR(255) NOT NULL,
    attempts INTEGER DEFAULT 0,
    used BOOLEAN DEFAULT false,
    created_at TIMESTAMPTZ,
    expires_at TIMESTAMPTZ
);

CREATE INDEX IF NOT EXISTS idx_otp_identifier ON otps(identifier);

CREATE TABLE IF NOT EXISTS mfa_configs (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    user_id BIGINT NOT NULL UNIQUE REFERENCES users(id) ON DELETE CASCADE,
    mfa_enabled BOOLEAN NOT NULL DEFAULT false,
    email_otp_enabled BOOLEAN NOT NULL DEFAULT false,
    mobile_otp_enabled BOOLEAN NOT NULL DEFAULT false,
    totp_enabled BOOLEAN NOT NULL DEFAULT false,
    totp_secret VARCHAR(64),
    totp_verified BOOLEAN NOT NULL DEFAULT false,
    created_at TIMESTAMPTZ NOT NULL,
    updated_at TIMESTAMPTZ NOT NULL
);

CREATE INDEX IF NOT EXISTS idx_mfa_user_id ON mfa_configs(user_id);

CREATE TABLE IF NOT EXISTS mfa_pending_sessions (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    session_token VARCHAR(64) NOT NULL UNIQUE,
    user_id BIGINT NOT NULL REFERENCES users(id) ON DELETE CASCADE,
    email_verified BOOLEAN NOT NULL DEFAULT false,
    mobile_verified BOOLEAN NOT NULL DEFAULT false,
    totp_verified BOOLEAN NOT NULL DEFAULT false,
    expires_at TIMESTAMPTZ NOT NULL,
    created_at TIMESTAMPTZ NOT NULL
);

CREATE INDEX IF NOT EXISTS idx_mfa_session_token ON mfa_pending_sessions(session_token);
CREATE INDEX IF NOT EXISTS idx_mfa_session_user ON mfa_pending_sessions(user_id);

CREATE TABLE IF NOT EXISTS login_attempts (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    identifier VARCHAR(255),
    ip_address VARCHAR(64),
    success BOOLEAN,
    attempted_at TIMESTAMPTZ
);

CREATE INDEX IF NOT EXISTS idx_la_identifier ON login_attempts(identifier);

CREATE TABLE IF NOT EXISTS device_fingerprints (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    user_id BIGINT REFERENCES users(id) ON DELETE CASCADE,
    device_id VARCHAR(255),
    user_agent VARCHAR(512),
    ip_address VARCHAR(64),
    last_seen_at TIMESTAMPTZ,
    trusted BOOLEAN
);

CREATE INDEX IF NOT EXISTS idx_df_user ON device_fingerprints(user_id);

CREATE TABLE IF NOT EXISTS referrals (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    uuid VARCHAR(36) NOT NULL UNIQUE,
    created_at TIMESTAMPTZ NOT NULL,
    updated_at TIMESTAMPTZ NOT NULL,
    version BIGINT,
    referrer_id BIGINT NOT NULL REFERENCES users(id),
    referred_id BIGINT NOT NULL REFERENCES users(id),
    status VARCHAR(30) NOT NULL DEFAULT 'PENDING',
    referral_code VARCHAR(50),
    notes VARCHAR(255),
    bonus_given BOOLEAN NOT NULL DEFAULT false
);

CREATE INDEX IF NOT EXISTS idx_ref_referrer ON referrals(referrer_id);
CREATE INDEX IF NOT EXISTS idx_ref_referred ON referrals(referred_id);
CREATE INDEX IF NOT EXISTS idx_ref_status ON referrals(status);

CREATE TABLE IF NOT EXISTS milestones (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    uuid VARCHAR(36) NOT NULL UNIQUE,
    created_at TIMESTAMPTZ NOT NULL,
    updated_at TIMESTAMPTZ NOT NULL,
    version BIGINT,
    name VARCHAR(255) NOT NULL,
    milestone_type VARCHAR(50) NOT NULL,
    target_value INTEGER NOT NULL,
    reward_amount NUMERIC(18,4) NOT NULL,
    description VARCHAR(255),
    is_active BOOLEAN NOT NULL DEFAULT true
);

CREATE TABLE IF NOT EXISTS milestone_statuses (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    uuid VARCHAR(36) NOT NULL UNIQUE,
    created_at TIMESTAMPTZ NOT NULL,
    updated_at TIMESTAMPTZ NOT NULL,
    version BIGINT,
    user_id BIGINT NOT NULL REFERENCES users(id),
    milestone_id BIGINT NOT NULL REFERENCES milestones(id),
    state VARCHAR(30) DEFAULT 'LOCKED',
    current_progress INTEGER,
    CONSTRAINT uq_milestone_status_user_milestone UNIQUE (user_id, milestone_id)
);

CREATE INDEX IF NOT EXISTS idx_mss_milestone ON milestone_statuses(milestone_id);
CREATE INDEX IF NOT EXISTS idx_mss_user ON milestone_statuses(user_id);

CREATE TABLE IF NOT EXISTS reward_config (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    uuid VARCHAR(36) NOT NULL UNIQUE,
    created_at TIMESTAMPTZ NOT NULL,
    updated_at TIMESTAMPTZ NOT NULL,
    version BIGINT,
    credits_per_rupee INTEGER NOT NULL DEFAULT 10,
    min_redeem_credits BIGINT NOT NULL DEFAULT 1000,
    max_daily_earn BIGINT NOT NULL DEFAULT 4000,
    redemption_wait_seconds BIGINT NOT NULL DEFAULT 0,
    payout_processing_seconds BIGINT NOT NULL DEFAULT 604800,
    is_active BOOLEAN NOT NULL DEFAULT true,
    redeem_window_start VARCHAR(8),
    redeem_window_end VARCHAR(8)
);

CREATE TABLE IF NOT EXISTS user_rewards (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    uuid VARCHAR(36) NOT NULL UNIQUE,
    created_at TIMESTAMPTZ NOT NULL,
    updated_at TIMESTAMPTZ NOT NULL,
    version BIGINT,
    user_id BIGINT NOT NULL UNIQUE REFERENCES users(id) ON DELETE CASCADE,
    total_credits BIGINT NOT NULL DEFAULT 0,
    redeemed_credits BIGINT NOT NULL DEFAULT 0,
    pending_credits BIGINT NOT NULL DEFAULT 0
);

CREATE INDEX IF NOT EXISTS idx_user_rewards_user ON user_rewards(user_id);

CREATE TABLE IF NOT EXISTS reward_transactions (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    uuid VARCHAR(36) NOT NULL UNIQUE,
    created_at TIMESTAMPTZ NOT NULL,
    updated_at TIMESTAMPTZ NOT NULL,
    version BIGINT,
    user_id BIGINT NOT NULL REFERENCES users(id),
    credits BIGINT NOT NULL,
    type VARCHAR(20) NOT NULL,
    source VARCHAR(30) NOT NULL,
    description VARCHAR(255),
    status VARCHAR(20) NOT NULL DEFAULT 'COMPLETED',
    reference_id VARCHAR(255) UNIQUE
);

CREATE INDEX IF NOT EXISTS idx_rtx_user ON reward_transactions(user_id);
CREATE INDEX IF NOT EXISTS idx_rtx_type ON reward_transactions(type);
CREATE INDEX IF NOT EXISTS idx_rtx_status ON reward_transactions(status);
CREATE INDEX IF NOT EXISTS idx_rtx_source ON reward_transactions(source);

CREATE TABLE IF NOT EXISTS redeem_log (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    user_id BIGINT NOT NULL,
    credits BIGINT NOT NULL,
    rupee_value NUMERIC(10,2) NOT NULL,
    payment_details VARCHAR(100) NOT NULL,
    reference_id VARCHAR(64) NOT NULL UNIQUE,
    status VARCHAR(30) NOT NULL DEFAULT 'INITIATED',
    fraud_flags VARCHAR(255) DEFAULT '',
    client_ip VARCHAR(45),
    created_at TIMESTAMPTZ NOT NULL,
    updated_at TIMESTAMPTZ NOT NULL
);

CREATE INDEX IF NOT EXISTS idx_rl_user_created ON redeem_log(user_id, created_at);
CREATE INDEX IF NOT EXISTS idx_rl_reference_id ON redeem_log(reference_id);
CREATE INDEX IF NOT EXISTS idx_rl_status ON redeem_log(status);

CREATE TABLE IF NOT EXISTS payout_requests (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    uuid VARCHAR(36) NOT NULL UNIQUE,
    created_at TIMESTAMPTZ NOT NULL,
    updated_at TIMESTAMPTZ NOT NULL,
    version BIGINT,
    user_id BIGINT NOT NULL REFERENCES users(id),
    amount NUMERIC(18,4) NOT NULL,
    payout_type VARCHAR(255) NOT NULL,
    payment_details VARCHAR(255) NOT NULL,
    status VARCHAR(20) NOT NULL DEFAULT 'PENDING',
    idempotency_key VARCHAR(128) UNIQUE,
    admin_notes VARCHAR(255),
    transaction_ref VARCHAR(255),
    processed_by VARCHAR(255)
);

CREATE INDEX IF NOT EXISTS idx_payout_user ON payout_requests(user_id);
CREATE INDEX IF NOT EXISTS idx_payout_status ON payout_requests(status);
CREATE INDEX IF NOT EXISTS idx_payout_idempotency ON payout_requests(idempotency_key);

CREATE TABLE IF NOT EXISTS notifications (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    uuid VARCHAR(36) NOT NULL UNIQUE,
    created_at TIMESTAMPTZ NOT NULL,
    updated_at TIMESTAMPTZ NOT NULL,
    version BIGINT,
    user_id BIGINT NOT NULL REFERENCES users(id) ON DELETE CASCADE,
    type VARCHAR(50) NOT NULL,
    title VARCHAR(255) NOT NULL,
    message VARCHAR(500) NOT NULL,
    is_read BOOLEAN NOT NULL DEFAULT false
);

CREATE INDEX IF NOT EXISTS idx_notif_user ON notifications(user_id);

CREATE TABLE IF NOT EXISTS campaigns (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    uuid VARCHAR(36) NOT NULL UNIQUE,
    created_at TIMESTAMPTZ NOT NULL,
    updated_at TIMESTAMPTZ NOT NULL,
    version BIGINT,
    name VARCHAR(255) NOT NULL,
    description TEXT,
    campaign_type VARCHAR(50),
    tracking_link VARCHAR(255) NOT NULL,
    reward_amount NUMERIC(18,4) NOT NULL,
    terms_url TEXT,
    logo_url VARCHAR(255),
    advertiser_name VARCHAR(255),
    start_at TIMESTAMPTZ,
    end_at TIMESTAMPTZ,
    is_active BOOLEAN DEFAULT true,
    max_leads_per_user INTEGER DEFAULT 1,
    total_leads_cap INTEGER DEFAULT 0
);

CREATE INDEX IF NOT EXISTS idx_campaign_active ON campaigns(is_active);
CREATE INDEX IF NOT EXISTS idx_campaign_type ON campaigns(campaign_type);

CREATE TABLE IF NOT EXISTS campaign_leads (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    uuid VARCHAR(36) NOT NULL UNIQUE,
    created_at TIMESTAMPTZ NOT NULL,
    updated_at TIMESTAMPTZ NOT NULL,
    version BIGINT,
    user_id BIGINT NOT NULL REFERENCES users(id),
    campaign_id BIGINT NOT NULL REFERENCES campaigns(id),
    status VARCHAR(30) NOT NULL DEFAULT 'PENDING',
    registered_mobile VARCHAR(255),
    registered_email VARCHAR(255),
    proof_url VARCHAR(255),
    admin_notes VARCHAR(255),
    rejection_reason VARCHAR(255)
);

CREATE INDEX IF NOT EXISTS idx_lead_user ON campaign_leads(user_id);
CREATE INDEX IF NOT EXISTS idx_lead_campaign ON campaign_leads(campaign_id);
CREATE INDEX IF NOT EXISTS idx_lead_status ON campaign_leads(status);

CREATE TABLE IF NOT EXISTS spin_prizes (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    uuid VARCHAR(36) NOT NULL UNIQUE,
    created_at TIMESTAMPTZ NOT NULL,
    updated_at TIMESTAMPTZ NOT NULL,
    version BIGINT,
    credits BIGINT NOT NULL,
    weight INTEGER NOT NULL,
    label VARCHAR(255) NOT NULL,
    color VARCHAR(255),
    is_active BOOLEAN NOT NULL DEFAULT true,
    sort_order INTEGER NOT NULL DEFAULT 0
);

CREATE TABLE IF NOT EXISTS spin_history (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    uuid VARCHAR(36) NOT NULL UNIQUE,
    created_at TIMESTAMPTZ NOT NULL,
    updated_at TIMESTAMPTZ NOT NULL,
    version BIGINT,
    user_id BIGINT NOT NULL REFERENCES users(id) ON DELETE CASCADE,
    credits_won BIGINT NOT NULL
);

CREATE INDEX IF NOT EXISTS idx_spin_user ON spin_history(user_id);

CREATE TABLE IF NOT EXISTS task_config (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    uuid VARCHAR(36) NOT NULL UNIQUE,
    created_at TIMESTAMPTZ NOT NULL,
    updated_at TIMESTAMPTZ NOT NULL,
    version BIGINT,
    task_type VARCHAR(50) NOT NULL UNIQUE,
    label VARCHAR(255) NOT NULL,
    description VARCHAR(500),
    credits BIGINT NOT NULL,
    category VARCHAR(255) NOT NULL DEFAULT 'DAILY',
    icon VARCHAR(10),
    partner_url VARCHAR(1000),
    requires_lead BOOLEAN NOT NULL DEFAULT false,
    is_active BOOLEAN NOT NULL DEFAULT true,
    sort_order INTEGER NOT NULL DEFAULT 0
);

CREATE TABLE IF NOT EXISTS daily_task_completions (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    uuid VARCHAR(36) NOT NULL UNIQUE,
    created_at TIMESTAMPTZ NOT NULL,
    updated_at TIMESTAMPTZ NOT NULL,
    version BIGINT,
    user_id BIGINT NOT NULL REFERENCES users(id) ON DELETE CASCADE,
    task_type VARCHAR(50) NOT NULL,
    credits_earned BIGINT NOT NULL
);

CREATE INDEX IF NOT EXISTS idx_task_user_date ON daily_task_completions(user_id, task_type);

CREATE TABLE IF NOT EXISTS task_leads (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    uuid VARCHAR(36) NOT NULL UNIQUE,
    created_at TIMESTAMPTZ NOT NULL,
    updated_at TIMESTAMPTZ NOT NULL,
    version BIGINT,
    user_id BIGINT NOT NULL REFERENCES users(id),
    task_type VARCHAR(50) NOT NULL,
    email VARCHAR(255) NOT NULL,
    mobile VARCHAR(255) NOT NULL,
    partner_url VARCHAR(1000)
);

CREATE INDEX IF NOT EXISTS idx_task_lead_user_task ON task_leads(user_id, task_type);

CREATE TABLE IF NOT EXISTS admin_audit_logs (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    uuid VARCHAR(36) NOT NULL UNIQUE,
    created_at TIMESTAMPTZ NOT NULL,
    updated_at TIMESTAMPTZ NOT NULL,
    version BIGINT,
    admin_email VARCHAR(255) NOT NULL,
    action_type VARCHAR(255) NOT NULL,
    target_user_id BIGINT NOT NULL,
    amount BIGINT,
    reason VARCHAR(500)
);

CREATE INDEX IF NOT EXISTS idx_aal_admin ON admin_audit_logs(admin_email);
CREATE INDEX IF NOT EXISTS idx_aal_target ON admin_audit_logs(target_user_id);
CREATE INDEX IF NOT EXISTS idx_aal_action ON admin_audit_logs(action_type);
